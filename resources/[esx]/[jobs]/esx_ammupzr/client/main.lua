      -- table.insert(elements, {label = _U('buy_weapons'), value = 'buy_weapons'})
    -- end

    ESX.UI.Menu.CloseAll()

    ESX.UI.Menu.Open(
      'default', GetCurrentResourceName(), 'armory',
      {
        title    = _U('armory'),
        align    = 'right',
        elements = elements,
      },
